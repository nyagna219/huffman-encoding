
public class Decnode {
	private int element;
	private Decnode left;
	private Decnode right;
	
	public Decnode(int element) {
		this.element = element;
	}
	public int getElement() {
		return element;
	}
	public void setElement(int element) {
		this.element = element;
	}
	public Decnode getLeft() {
		return left;
	}
	public void setLeft(Decnode left) {
		this.left = left;
	}
	public Decnode getRight() {
		return right;
	}
	public void setRight(Decnode right) {
		this.right = right;
	}
	

}
